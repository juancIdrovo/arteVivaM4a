package ec.tecazuay.artevivam4a.solicitud;

public class LoginRespuesta {
    String perPrimerNom;
    String perSegundoNom;
    String perTelefono;
    String rol_nombre;
    String rol_estado;
    public String getPerPrimerNom() {
        return perPrimerNom;
    }
    public void setPerPrimerNom(String perPrimerNom) {
        this.perPrimerNom = perPrimerNom;
    }
    public String getPerSegundoNom() {
        return perSegundoNom;
    }
    public void setPerSegundoNom(String perSegundoNom) {
        this.perSegundoNom = perSegundoNom;
    }
    public String getPerTelefono() {
        return perTelefono;
    }
    public void setPerTelefono(String perTelefono) {
        this.perTelefono = perTelefono;
    }
    public String getRol_nombre() {
        return rol_nombre;
    }
    public void setRol_nombre(String rol_nombre) {
        this.rol_nombre = rol_nombre;
    }
    public String getRol_estado() {
        return rol_estado;
    }
    public void setRol_estado(String rol_estado) {
        this.rol_estado = rol_estado;
    }
}
